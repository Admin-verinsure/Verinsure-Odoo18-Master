async function getFormCustomizerTranslations() {
    let userLang = 'en_US';
    
    try {
        if (typeof odoo !== 'undefined' && odoo.csrf_token) {
            const response = await fetch('/web/session/get_session_info', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'X-CSRF-Token': odoo.csrf_token
                },
                body: JSON.stringify({
                    jsonrpc: '2.0',
                    method: 'call',
                    params: {}
                })
            });
            
            const result = await response.json();
            userLang = result.result?.user_context?.lang || 'en_US';
        } else {
            const response = await fetch('/web/session/get_session_info');
            const result = await response.json();
            userLang = result.user_context?.lang || result.result?.user_context?.lang || 'en_US';
        }
    } catch (error) {
        console.warn('Could not fetch user language, using English:', error);
    }
    
    return FORM_CUSTOMIZER_TRANSLATIONS[userLang] || FORM_CUSTOMIZER_TRANSLATIONS['en_US'];
}

window.getFormCustomizerTranslations = getFormCustomizerTranslations;
